CREATE DATABASE australauto;

\c australauto

CREATE TABLE usuarios (
    id_usuario SERIAL PRIMARY KEY,
    nombre VARCHAR(30) NOT NULL,
    apellido VARCHAR(30) NOT NULL,
    email VARCHAR(30) NOT NULL UNIQUE,
    foto VARCHAR,
    telefono INTEGER,
    password VARCHAR
);

/* ALTER TABLE usuarios RENAME COLUMN contraseña TO password; */

CREATE TABLE marcas (
    id_marca SERIAL PRIMARY KEY,
    nombre VARCHAR(50)
);

CREATE TABLE modelos (
    id_modelo SERIAL PRIMARY KEY,
    nombre VARCHAR(50),
    id_marca INT NOT NULL REFERENCES marcas(id_marca)
);

CREATE TABLE transmisiones (
    id_transmision SERIAL PRIMARY KEY,
    nombre VARCHAR(50)
);

CREATE TABLE categorias (
    id_categoria SERIAL PRIMARY KEY,
    nombre VARCHAR(50)
);

CREATE TABLE estados (
    id_estado SERIAL PRIMARY KEY,
    nombre VARCHAR(50)
);

CREATE TABLE publicaciones (
    id_publicacion SERIAL PRIMARY KEY,
    id_usuario INT NOT NULL REFERENCES usuarios(id_usuario),
    precio INTEGER,
    id_marca INT NOT NULL REFERENCES marcas(id_marca),
    id_modelo INT NOT NULL REFERENCES modelos(id_modelo),
    year INTEGER,
    kilometraje INTEGER,
    id_transmision INT NOT NULL REFERENCES transmisiones(id_transmision),
    id_categoria INT NOT NULL REFERENCES categorias(id_categoria),
    id_estado INT NOT NULL REFERENCES estados(id_estado),
    descripcion TEXT,
    imagen VARCHAR(255)
);

